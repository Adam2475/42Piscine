#include <unistd.h>

int	ft_str_is_alpha(char *str)
{
	int	i;

	i = 0;
	if (!(str))
		return (0);
	while (str[i] != '\0')
	{
		if (!(((str[i] >= 'a') && (str[i] <= 'z'))
				|| ((str[i] >= 65) && (str[i] <= 90))))
			return (0);
		else
			i++;
	}
	return (1);
}
